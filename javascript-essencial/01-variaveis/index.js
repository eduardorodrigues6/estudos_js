/*
  Em javascript declaramos as váriaveis com os operadores ou palavra reservadas 
  let e const (de acordo com ES6), pois antes qualquer váriavel era declarada 
  somente com var, Que pode receber qualquer valor em qualquer momento do software 
  e impactando todo fluxo na programação.

  A diferença entre var e let, é que "var" pode sofrer mudanças no decorrer do 
  software sem dar erro, em qualquer escopo, já o let sendo declarada no 
  mesmo escopo e declarada duas vezes pode sofrer erro de declaração na 
  execução do algoritmo;
*/

